	private void BtnSubmitEnterpriseAdminActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BtnSubmitEnterpriseAdminActionPerformed
		// TODO add your handling code here:
		Enterprise enterprise = (Enterprise) BoxManageAdminEnterpriseList.getSelectedItem();

		String username = TFManageAdminUsername.getText();
		String password = String.valueOf(TFManageAdminPassword.getText());
		String name = TFManageAdminName.getText();
		if (username.isEmpty() || password.isEmpty() || name.isEmpty()) {
			JOptionPane.showMessageDialog(null, "Please enter all fields", "Warning", JOptionPane.WARNING_MESSAGE);
		} else {
			Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);

			if (system.checkIfUserIsUnique(username)) {
				UserAccount account = null;
				if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Maintanence) {
					account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee,
							new MaintenanceAdmin());
				} else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Cleaning) {
					account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee,
							new CleaningAdmin());
				} else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.SpareParts) {
					account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee,
							new SparePartsAdmin());
				} else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Accounts) {
					account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee,
							new AccountsAdmin());
				}

				TFManageAdminUsername.setText("");
				TFManageAdminPassword.setText("");
				TFManageAdminName.setText("");
				JOptionPane.showMessageDialog(null, "Account created sucessfully");
				populateTable();
			} else {
				JOptionPane.showMessageDialog(null, "Please enter unique username", "Warning",
						JOptionPane.WARNING_MESSAGE);
			}
		}

	}// GEN-LAST:event_BtnSubmitEnterpriseAdminActionPerformed

