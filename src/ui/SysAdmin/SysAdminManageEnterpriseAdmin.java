	private void populateTable() {
		TableManageEnterpriseAdmin.getTableHeader().setDefaultRenderer(new HeaderColors());
		DefaultTableModel model = (DefaultTableModel) TableManageEnterpriseAdmin.getModel();

		model.setRowCount(0);
		for (Network network : system.getNetworkList()) {
			for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
				for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
					Object[] row = new Object[3];
					row[0] = enterprise.getName();
					row[1] = network.getName();
					row[2] = userAccount.getUsername();

					model.addRow(row);
				}
			}
		}
	}

	private void populateNetworkComboBox() {
		BoxManageAdminNetworkList.removeAllItems();
		BoxManageAdminEnterpriseList.removeAllItems();

		for (Network network : system.getNetworkList()) {
			BoxManageAdminNetworkList.addItem(network);
		}
	}

	private void populateEnterpriseComboBox(Network network) {
		BoxManageAdminEnterpriseList.removeAllItems();

		for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
			BoxManageAdminEnterpriseList.addItem(enterprise);
		}

	}

